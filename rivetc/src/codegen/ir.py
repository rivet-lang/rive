class ArrayLit:
    def __init__(self, typ, elems, is_variadic_init = False):
        self.typ = typ
        self.elems = elems
        self.is_variadic_init = is_variadic_init

    def __repr__(self):
        if self.is_variadic_init:
            return f"{self.typ}... [{', '.join([str(e) for e in self.elems])}]"
        return f"{self.typ} [{', '.join([str(e) for e in self.elems])}]"

    def __str__(self):
        return self.__repr__()

